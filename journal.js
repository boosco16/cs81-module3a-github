// Greet a user by name 
function greet(name) { 
  return  "Hello, " + name + "!";
}
// Add two numbers
function add(a, b) {
  return a + b;
}
// Check if a number is even 
function isEven(num) {
  return num % 2 === 0;
}
//Reverse a string 
function reverseString(str) {
  return str.split("").reverse().join("")
}
// Find the max of two numbers
function max(a, b) { 
  return a > b ? a : b;
}
